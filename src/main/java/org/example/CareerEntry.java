package org.example;

public class CareerEntry {
    public int id;
    public String date;
    public String guesses;
    public String colors;
    public String word;

    public CareerEntry(int id, String date, String guesses, String colors, String word) {
        this.id = id;
        this.date = date;
        this.guesses = guesses;
        this.colors = colors;
        this.word = word;
    }
}
